String getMonthYear(DateTime start, DateTime end) {
  const months = [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December',
  ];

  if (start.month == end.month) {
    return '${months[start.month - 1]} ${start.year}';
  } else {
    return '${months[start.month - 1]} - ${months[end.month - 1]} ${start.year}';
  }
}

